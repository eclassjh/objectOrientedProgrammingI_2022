#include <iostream>
using namespace std;

int main() {
	int a = 0.5;

	switch (a)
	{
	case 0.5 :
		cout << "The art of C" << endl;
		break;

	case 1.5 :
		cout << "The spirit of C" << endl;
		break;

	case 2.5 :
		cout << "See through C" << endl;
		break;

	case 3.5 :
		cout << "Simply C" << endl;
		break;
	}
}