# include <iostream>
using namespace std;

int main() {
	int i = 3;
	switch (i)
	{
	case 1:
		cout << "I am in case 1. " << endl;
		break;
	case 2:
		cout << "I am in case 2. " << endl;
		break;
	case 3:
		cout << "I am in case 3. " << endl;
		break;
	default :
		cout << "I am in case default. " << endl;
		break;
	}

}